        <h1>Piano vaccini anti Covid-19 in Italia</h1>
        <p>Il ministro della Salute Roberto Speranza ha presentato il 2 dicembre 2020 al Parlamento le linee guida del Piano strategico per la vaccinazione anti-SARS-CoV-2/COVID-19 (Decreto 2 gennaio 2021), elaborato da Ministero della Salute, Commissario Straordinario per l’Emergenza, Istituto Superiore di Sanità, Agenas e Aifa.  
        Il 13 marzo 2021 è stato diffuso il Piano del Commissario straordinario per l’esecuzione della campagna vaccinale nazionale. Elaborato in armonia con il Piano strategico nazionale del Ministero della Salute, fissa le linee operative per completare al più presto la campagna vaccinale.</p>
