        <h1>Vaccino anti COVID-19</h1>
        <p> Un vaccino anti COVID-19 è un vaccino in grado di indurre l'immunità acquisita contro la COVID-19, causata dal virus SARS-CoV-2.

        La scoperta e lo sviluppo di questi vaccini sono diventati gli obiettivi di un vasto sforzo scientifico a livello mondiale, in seguito al diffondersi della pandemia di COVID-19 iniziata nel 2019, che ha scatenato inoltre ulteriori indagini per capire dove e come essa avrebbe potuto infettare la popolazione mondiale e come non sia trasmissibile anche tra gli animali.

        A marzo 2021, 12 vaccini erano stati autorizzati da almeno un'autorità nazionale di regolamentazione per l'uso pubblico: 2 vaccini a RNA (il vaccino Pfizer-BioNTech - il primo approvato per l'uso regolare[1] - e il vaccino Moderna), 4 vaccini inattivati convenzionali (BBIBP-CorV di Sinopharm, BBV152 di Bharat Biotech, CoronaVac di Sinovac e CoviVac), 4 vaccini a vettore virale (Sputnik V dell'Istituto di ricerca Gamaleya, il vaccino Oxford-AstraZeneca, Ad5-nCoV della CanSino e il vaccino Johnson & Johnson) e due vaccini a subunità proteiche (EpiVacCorona dell'Istituto Vektor e ZF2001).

        Al 10 marzo 2021, erano state somministrate oltre 328 milioni di singole dosi nel mondo, con Israele, seguito da Emirati Arabi Uniti e Bahrein, come Stato con più singole dosi somministrate in rapporto alla popolazione.[3][4][5]

        Pfizer, Moderna e AstraZeneca hanno previsto una capacità di produzione di 5,3 miliardi di dosi nel 2021, che potrebbe essere utilizzata per vaccinare circa 3 miliardi di persone (poiché i vaccini richiedono due dosi per un effetto protettivo contro la COVID-19).[6] A dicembre 2020, più di 10 miliardi di dosi di vaccino erano state preordinate dai vari Paesi.
        </p>
