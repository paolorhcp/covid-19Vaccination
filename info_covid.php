        <h1>Che cos'è il COVID-19</h1>
            <p> 
                La COVID-19, acronimo dell'inglese COronaVIrus Disease 19, conosciuta anche come malattia respiratoria acuta da SARS-CoV-2 o malattia da coronavirus 2019, è una malattia infettiva respiratoria causata dal virus denominato SARS-CoV-2 appartenente alla famiglia dei coronavirus. I primi casi sono stati riscontrati in Cina durante la pandemia di COVID-19. Al 2 febbraio 2021 il suo tasso apparente di letalità è del 2,16%.

                Il periodo di incubazione mediamente è di 5,1 giorni (IC 96% 4,1 - 7,0 giorni) con il 95° percentile di 12,5 giorni. Per questo motivo è stato indicato un periodo di isolamento di 14 giorni dalla possibile esposizione a un caso sospetto o accertato. Durante il periodo di incubazione si è contagiosi, successivamente possono comparire i sintomi. Il virus si trasmette per via aerea, molto spesso tramite le goccioline respiratorie. Per limitarne la trasmissione devono essere prese precauzioni, come mantenere la distanza interpersonale di almeno 1,5 metri e tenere comportamenti corretti sul piano dell’igiene personale (lavare e disinfettare periodicamente le mani, starnutire o tossire in un fazzoletto o nell'incavo del gomito, indossare mascherine e guanti) e ambientale (rinnovare spesso l'aria negli ambienti chiusi aprendo le finestre e mantenere gli ambienti molto puliti). I governi e gli enti competenti consigliano a coloro che ritengono di essere infetti di rimanere in isolamento fiduciario, indossare una mascherina chirurgica, osservare le regole di igiene e contattare quanto prima un medico al fine di ricevere ulteriori indicazioni.

                 Il virus colpisce principalmente il tratto respiratorio superiore e inferiore ma può provocare sintomi che riguardano tutti gli organi e apparati. In oltre la metà dei casi l'infezione decorre in maniera del tutto asintomatica e in circa un terzo dei casi presenta sintomi simil-influenzali (forma pauci-sintomatica). In una minoranza di casi (circa 5-6% dei casi) invece la malattia può manifestarsi in forma moderata o grave con rischio di complicanze soprattutto respiratorie (insufficienza respiratoria, ARDS).

                I sintomi simil-influenzali, più frequenti sono: febbre, tosse, cefalea (mal di testa), dispnea (respiro corto), artralgie e mialgie (dolore ad articolazioni e ai muscoli), astenia (stanchezza) e disturbi gastrointestinali quali la diarrea; sintomi caratteristici della patologia COVID-19 sono l'anosmia (perdita dell'olfatto) e l'ageusia (perdita del gusto), transitorie.

                Nei casi più gravi può verificarsi una polmonite, una sindrome da distress respiratorio acuto, sepsi e uno shock settico fino ad arrivare al decesso del paziente. Sono in fase di sperimentazione oltre 76 vaccini specifici per questa malattia, di cui:

                40 in fase 1 (studio delle caratteristiche del farmaco)
                17 in fase 2 (studio sulla sicurezza del farmaco)
                13 in fase 3 (studio sull'efficacia del farmaco)
                6 (approvati per uso limitato) in fase 4 (studio di farmaco-sorveglianza).
                Il trattamento consiste nell'isolare il paziente per prevenire la diffusione del contagio e nel curare i sintomi clinici.
