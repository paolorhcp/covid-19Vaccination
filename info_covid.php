                <h1>Pandemia di COVID-19 in Italia</h1>
                    <p> Con pandemia di COVID-19 in Italia si fa riferimento alla diffusione in Italia della malattia infettiva COVID-19 in corso dal 2020.


                        I primi due casi italiani della pandemia sono stati confermati il 30 gennaio 2020, quando due turisti provenienti dalla Cina sono risultati positivi al virus SARS-CoV-2 a Roma. Un focolaio di infezioni di COVID-19 è stato successivamente rilevato il 21 febbraio 2020 a partire da 16 casi confermati a Codogno (LO) in Lombardia, aumentati a 60 il giorno successivo con i primi decessi segnalati il 22 febbraio a Casalpusterlengo (LO) e a Vo' (PD).

                        Alla data del 16 marzo 2021 sono stati registrati 3 258 770 casi positivi, tra cui 2 619 654 dimessi e guariti, 103 001 deceduti, 536 115 casi attivi e, sempre al 16 marzo, sono stati effettuati 37 564 451 tamponi molecolari e 7 607 243 tamponi antigenici rapidi, rendendo l'Italia il settimo paese al mondo e il quarto in Europa per numero di casi totali e complessivamente il sesto paese al mondo e il secondo in Europa per numero di decessi.

                        Al 16 marzo 2021, 2 145 434 persone (3,61% della popolazione) hanno completato la vaccinazione anti COVID-19 e 7 039 518 singole dosi sono state somministrate, rendendo l'Italia il quattordicesimo paese al mondo e il decimo in Europa per percentuale di popolazione vaccinata, l'ottavo paese al mondo e il quinto in Europa per numero di persone completamente vaccinate, il dodicesimo paese al mondo e il sesto in Europa per numero di dosi somministrate.
